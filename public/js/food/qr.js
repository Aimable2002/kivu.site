let _qrLoaded = false;

export function generateQR(restaurantId) {
    const modal = document.getElementById('qr-modal');
    const box   = document.getElementById('qr-box');
    if (!modal || !box) return;

    // URL always uses ?id= so anyone scanning cold can load the page directly
    const url = `${window.location.origin}/food/restaurant?id=${restaurantId}`;
    document.getElementById('qr-url-label').textContent = url;

    modal.classList.remove('hidden');
    box.innerHTML = '';

    if (_qrLoaded) {
        renderQR(box, url);
        return;
    }

    const script  = document.createElement('script');
    script.src    = 'https://cdnjs.cloudflare.com/ajax/libs/qrcodejs/1.0.0/qrcode.min.js';
    script.onload = () => { _qrLoaded = true; renderQR(box, url); };
    document.head.appendChild(script);
}

function renderQR(box, url) {
    new QRCode(box, {
        text:         url,
        width:        220,
        height:       220,
        colorDark:    '#1e293b',
        colorLight:   '#ffffff',
        correctLevel: QRCode.CorrectLevel.H
    });
}

export function closeQR() {
    document.getElementById('qr-modal')?.classList.add('hidden');
}

export function printQR() {
    const qrBox  = document.getElementById('qr-box');
    const url    = document.getElementById('qr-url-label')?.textContent || '';
    const name   = document.getElementById('resto-name')?.textContent   || 'Restaurant';

    // Get the QR canvas or img generated by qrcodejs
    const canvas = qrBox?.querySelector('canvas');
    const img    = qrBox?.querySelector('img');
    const qrSrc  = canvas ? canvas.toDataURL('image/png') : img?.src || '';

    if (!qrSrc) return;

    const win = window.open('', '_blank', 'width=400,height=550');
    win.document.write(`
    <!DOCTYPE html>
    <html>
    <head>
        <title>QR Code — ${name}</title>
        <style>
            * { margin:0; padding:0; box-sizing:border-box }
            body {
                font-family: system-ui, sans-serif;
                display: flex; flex-direction: column;
                align-items: center; justify-content: center;
                min-height: 100vh; padding: 32px; text-align: center;
                background: #fff;
            }
            .logo { font-size: 11px; font-weight: 800; color: #FF7F50; letter-spacing: 2px; text-transform: uppercase; margin-bottom: 20px }
            h1 { font-size: 22px; font-weight: 900; color: #111; margin-bottom: 6px }
            .sub { font-size: 12px; color: #6b7280; margin-bottom: 24px }
            img.qr { width: 220px; height: 220px; border: 3px solid #f3f4f6; border-radius: 16px; padding: 12px }
            .url { font-size: 9px; color: #9ca3af; margin-top: 16px; word-break: break-all; max-width: 260px }
            .cta { margin-top: 20px; font-size: 13px; font-weight: 700; color: #111 }
            .cta span { color: #FF7F50 }
            @media print {
                body { padding: 20px }
            }
        </style>
    </head>
    <body>
        <div class="logo">Kivu Food</div>
        <h1>${name}</h1>
        <p class="sub">Scan to view menu &amp; order</p>
        <img class="qr" src="${qrSrc}" alt="QR Code">
        <p class="url">${url}</p>
        <p class="cta">Scan with your camera &amp; order via <span>WhatsApp</span></p>
        <script>window.onload = () => { window.print(); }</script>
    </body>
    </html>`);
    win.document.close();
}